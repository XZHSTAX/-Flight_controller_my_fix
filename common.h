#ifndef _COMMON_H_
#define _COMMON_H_

/* DriverLib Includes */
#include <ti/devices/msp432e4/driverlib/driverlib.h>

/* Standard Includes */
#include <stdint.h>
#include <stdbool.h>

/* Data Type Declaration */
typedef uint8_t     u8;     /*  8 bits */
typedef uint16_t    u16;    /* 16 bits */
typedef uint32_t    u32;    /* 32 bits */
typedef uint64_t    u64;    /* 64 bits */

typedef int8_t      s8;     /*  8 bits */
typedef int16_t     s16;    /* 16 bits */
typedef int32_t     s32;    /* 32 bits */
typedef int64_t     s64;    /* 64 bits */

typedef volatile int8_t     vs8;    /*  8 bits */
typedef volatile int16_t    vs16;   /* 16 bits */
typedef volatile int32_t    vs32;   /* 32 bits */
typedef volatile int64_t    vs64;   /* 64 bits */

typedef volatile uint8_t    vu8;    /*  8 bits */
typedef volatile uint16_t   vu16;   /* 16 bits */
typedef volatile uint32_t   vu32;   /* 32 bits */
typedef volatile uint64_t   vu64;   /* 64 bits */

#endif

